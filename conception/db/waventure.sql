CREATE EXTENSION unaccent;
CREATE EXTENSION
IF NOT EXISTS pgcrypto;
CREATE EXTENSION
IF NOT EXISTS "uuid-ossp";
SET client_encoding
TO 'UTF-8';

CREATE TABLE account
(
    account_id UUID,
    username varchar(25),
    first_name varchar(25),
    last_name varchar(25),
    email varchar(100) UNIQUE NOT NULL,
    birth_date date,
    avatar_id varchar(64),
    password varchar(64)
);

CREATE TABLE avatar
(
    avatar_id serial NOT NULL PRIMARY KEY,
    avatar_path varchar(64),
    FOREIGN KEY (avatar_id) REFERENCES avatar (avatar_id)
);

/* non fini (main_color) */
CREATE TABLE serie
(
    serie_id serial NOT NULL PRIMARY KEY,
    title varchar(100),
    image varchar(150),
    image_lg varchar(150),
    image_bg varchar(150),
    season smallint,
    author varchar(60),
    duration time,
    upload_date date,
    creation_date date
);

CREATE TABLE season
(
    season_id serial NOT NULL PRIMARY KEY,
    serie_id serial,
    season_nb smallint,
    quantite smallint,
    FOREIGN KEY (serie_id) REFERENCES serie (serie_id)
);


CREATE TABLE episode
(
    episode_id serial NOT NULL PRIMARY KEY,
    season_id serial,
    episode_title varchar(100),
    episode_nb smallint,
    duration time,
    mp3_file varchar(255),
    FOREIGN KEY (season_id) REFERENCES season (season_id)
);

CREATE TABLE favorite
(
    favorite_id serial NOT NULL PRIMARY KEY,
    account_id serial,
    serie_id serial,
    FOREIGN KEY (account_id) REFERENCES account (account_id),
    FOREIGN KEY (serie_id) REFERENCES serie (serie_id)
);

CREATE TABLE synopsis
(
    synopsis_id serial NOT NULL PRIMARY KEY,
    serie_id serial,
    body text,
    FOREIGN KEY (serie_id) REFERENCES serie (serie_id)
);

CREATE TABLE category
(
    category_id serial NOT NULL PRIMARY KEY,
    name varchar(100)
);

CREATE TABLE serie_category
(
    serie_category_id serial NOT NULL PRIMARY KEY,
    serie_id serial,
    category_id serial,
    FOREIGN KEY (serie_id) REFERENCES serie (serie_id),
    FOREIGN KEY (category_id) REFERENCES category (category_id)
);

CREATE TABLE actor
(
    actor_id serial NOT NULL PRIMARY KEY,
    name varchar(100)
);

CREATE TABLE serie_actor
(
    serie_actor_id serial NOT NULL PRIMARY KEY,
    serie_id serial,
    actor_id serial,
    FOREIGN KEY (serie_id) REFERENCES serie (serie_id),
    FOREIGN KEY (actor_id) REFERENCES actor (actor_id)
);

CREATE TABLE role
(
    role_id serial NOT NULL PRIMARY KEY,
    serie_id serial,
    actor_id serial,
    character varchar(100),
    FOREIGN KEY (serie_id) REFERENCES serie (serie_id),
    FOREIGN KEY (actor_id) REFERENCES actor (actor_id)
);

CREATE TABLE LISTEN
(
    listen_id serial NOT NULL PRIMARY KEY,
    account_id serial,
    episode_id serial,
    duration time,
    FOREIGN KEY (account_id) REFERENCES account (account_id),
    FOREIGN KEY (episode_id) REFERENCES episode (episode_id)
);

